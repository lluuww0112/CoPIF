from __future__ import annotations

import argparse
from pathlib import Path
import sys

from model.base import eval as base_eval


def _resolve_default_input_csv(config) -> Path:
    eval_config = config.get("eval", {})
    invoke_config = config.get("invoke", {})
    train_config = config.get("train", {})

    input_csv_value = eval_config.get("input_csv", None)
    if input_csv_value is not None:
        return base_eval._resolve_project_path(str(input_csv_value))

    dataset = str(eval_config.get("dataset", invoke_config.get("dataset", train_config.get("dataset", "refcoco"))))
    splitby = str(eval_config.get("splitby", invoke_config.get("splitby", train_config.get("splitby", "unc"))))
    split = str(eval_config.get("split", invoke_config.get("split", train_config.get("split", "val"))))
    checkpoint_dir = base_eval._resolve_project_path(
        str(eval_config.get("checkpoint_dir", invoke_config.get("checkpoint_dir", train_config.get("checkpoint_dir", "history/comparison"))))
    )
    return checkpoint_dir / f"inference_{dataset}_{splitby}_{split}.csv"


def _hydra_entry(config) -> None:
    eval_config = config.get("eval", {})
    input_csv = _resolve_default_input_csv(config)
    output_json_value = eval_config.get("output_json", None)
    output_json = (
        base_eval._resolve_project_path(str(output_json_value))
        if output_json_value is not None
        else None
    )
    visualization_dir_value = eval_config.get("visualization_dir", None)
    visualization_dir = (
        base_eval._resolve_project_path(str(visualization_dir_value))
        if visualization_dir_value is not None
        else input_csv.parent / f"{input_csv.stem}_visualizations"
    )
    thresholds = base_eval._resolve_thresholds(eval_config.get("iou_thresholds", None))
    clip_to_image = bool(eval_config.get("clip_to_image", True))
    worst_k = int(eval_config.get("worst_k", 5))
    visualization_count = int(eval_config.get("visualization_count", 10))
    visualization_seed = int(eval_config.get("visualization_seed", 42))

    base_eval._run_evaluation(
        input_csv=input_csv,
        output_json=output_json,
        thresholds=thresholds,
        clip_to_image=clip_to_image,
        worst_k=worst_k,
        visualization_dir=visualization_dir,
        visualization_count=visualization_count,
        visualization_seed=visualization_seed,
    )


def _parse_cli_args(argv: list[str]) -> argparse.Namespace:
    parser = argparse.ArgumentParser(
        description="Summarize IoU and bbox errors from an inference CSV generated by model/comparison/invoke.py."
    )
    parser.add_argument("--input-csv", type=str, default=None, help="Path to the inference CSV.")
    parser.add_argument(
        "--checkpoint-dir",
        type=str,
        default="history/comparison",
        help="Directory to search for the latest inference CSV when --input-csv is omitted.",
    )
    parser.add_argument("--output-json", type=str, default=None, help="Optional path to save the summary as JSON.")
    parser.add_argument(
        "--iou-thresholds",
        type=float,
        nargs="+",
        default=[0.25, 0.5, 0.75],
        help="IoU thresholds used for accuracy metrics.",
    )
    parser.add_argument(
        "--clip-to-image",
        dest="clip_to_image",
        action="store_true",
        help="Clip predicted and GT boxes to image boundaries before IoU computation.",
    )
    parser.add_argument(
        "--no-clip-to-image",
        dest="clip_to_image",
        action="store_false",
        help="Do not clip boxes to image boundaries before IoU computation.",
    )
    parser.set_defaults(clip_to_image=True)
    parser.add_argument("--worst-k", type=int, default=5, help="Number of lowest-IoU rows to print.")
    parser.add_argument(
        "--visualization-dir",
        type=str,
        default=None,
        help="Directory where sampled visualization PNG files will be saved. Defaults to <input_csv_stem>_visualizations.",
    )
    parser.add_argument(
        "--visualization-count",
        type=int,
        default=10,
        help="Number of random images to visualize. Set to 0 to disable image outputs.",
    )
    parser.add_argument(
        "--visualization-seed",
        type=int,
        default=42,
        help="Random seed used when sampling images for visualization.",
    )
    return parser.parse_args(argv)


def _cli_entry(argv: list[str]) -> None:
    args = _parse_cli_args(argv)
    project_root = Path.cwd()

    if args.input_csv is not None:
        input_csv = base_eval._resolve_project_path(args.input_csv, project_root=project_root)
    else:
        checkpoint_dir = base_eval._resolve_project_path(args.checkpoint_dir, project_root=project_root)
        input_csv = base_eval._find_latest_inference_csv(checkpoint_dir=checkpoint_dir)

    output_json = (
        base_eval._resolve_project_path(args.output_json, project_root=project_root)
        if args.output_json is not None
        else None
    )
    visualization_dir = (
        base_eval._resolve_project_path(args.visualization_dir, project_root=project_root)
        if args.visualization_dir is not None
        else input_csv.parent / f"{input_csv.stem}_visualizations"
    )
    base_eval._run_evaluation(
        input_csv=input_csv,
        output_json=output_json,
        thresholds=base_eval._resolve_thresholds(args.iou_thresholds),
        clip_to_image=bool(args.clip_to_image),
        worst_k=int(args.worst_k),
        visualization_dir=visualization_dir,
        visualization_count=int(args.visualization_count),
        visualization_seed=int(args.visualization_seed),
    )


if base_eval.hydra is not None:
    @base_eval.hydra.main(version_base=None, config_path="../../config", config_name="comparison")
    def main(config) -> None:
        _hydra_entry(config)
else:
    main = None


if __name__ == "__main__":
    use_argparse = base_eval.hydra is None or any(argument.startswith("--") for argument in sys.argv[1:])
    if use_argparse:
        _cli_entry(sys.argv[1:])
    elif main is not None:
        main()
